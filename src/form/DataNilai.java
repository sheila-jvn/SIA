    protected void datatable() {
        Object[] Baris = {"ID", "Nama", "NIS", "Kelas", "Nilai Tugas", "Nilai UTS", "Nilai UAS", "Rata-Rata"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txtCari.getText();

        try {
            String sql = "Select * FROM data_nilai where id like '%" + cariitem + "%' or nama like '%" + cariitem + "%' order by id asc";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5),
                    hasil.getString(6),
                    hasil.getString(7),
                    hasil.getString(8),});
            }
            tblNilai.setModel(tabmode);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data gagal dipanggil" + e);
        }
    }
    
    private void hitungRataRata() {
        try {
            float tugas = Float.parseFloat(txtTugas.getText());
            float uts = Float.parseFloat(txtUTS.getText());
            float uas = Float.parseFloat(txtUAS.getText());

            float rataRata = (tugas + uts + uas) / 3;
            txtRataRata.setText(String.format("%.2f", rataRata));
        } catch (NumberFormatException e) {
            // Kosongkan jika input belum lengkap atau tidak valid
            txtRataRata.setText("");
        }
    }


