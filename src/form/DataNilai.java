    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String sql = "insert into data_nilai (id, nama, nis, kelas, nilai_tugas, nilai_uts, nilai_uas, rata_rata) values (?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);

            stat.setString(1, txtid.getText());
            stat.setString(2, txtnama.getText());
            stat.setString(3, txtnis.getText());
            stat.setString(4, comboKls.getSelectedItem().toString());
            stat.setString(5, txtTugas.getText());
            stat.setString(6, txtUTS.getText());
            stat.setString(7, txtUAS.getText());
            stat.setString(8, txtRataRata.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            txtid.requestFocus();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "data gagal disimpan " + e);
        }
        datatable();
    }//GEN-LAST:event_bsimpanActionPerformed

